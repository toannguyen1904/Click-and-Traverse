"""Run all G1CaTra specialist evaluations and save videos to a folder.

Usage:
    python eval_all_catra.py                          # run all discovered experiments
    python eval_all_catra.py --output_dir my_videos   # custom output dir
    python eval_all_catra.py --num_steps 300          # shorter episodes
    python eval_all_catra.py --filter bar1            # only experiments whose name contains 'bar1'

Checkpoint naming convention (auto-generated by train_ppo.py):
    <MMDDHHM>_G1CaTra_<exp_name>x...
Example:
    04012130_G1CaTra_G1CaTra_bar1xG10xL10xO10xT004xdataassetsTypiObsbar1
"""
import os

os.environ.setdefault("MUJOCO_GL", "egl")
xla_flags = os.environ.get("XLA_FLAGS", "")
xla_flags += " --xla_gpu_triton_gemm_any=true"
os.environ["XLA_FLAGS"] = xla_flags

import argparse
import re
from pathlib import Path

import imageio
import mujoco
import numpy as np
import onnxruntime as rt

import cat_ppo
from cat_ppo.envs.g1.play_cat import PlayG1CaTraEnv


# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

_TYPI_OBS = "data/assets/TypiObs"
_RAND_OBS  = "data/assets/RandObs"
_LOG_ROOT  = Path("data/logs/origin")

# Regex to extract the obs_path suffix embedded in auto-generated experiment names.
# e.g. "...xdataassetsTypiObsbar1" → "data/assets/TypiObs/bar1"
_OBS_RE = re.compile(r"xdataassets(TypiObs|RandObs)(.+)$")


# ---------------------------------------------------------------------------
# Experiment discovery
# ---------------------------------------------------------------------------

def _decode_obs_path(exp_name: str) -> str | None:
    """Recover the obs_path from the experiment name suffix produced by train_ppo.py."""
    m = _OBS_RE.search(exp_name)
    if m is None:
        return None
    obs_type = "TypiObs" if m.group(1) == "TypiObs" else "RandObs"
    scene = m.group(2)
    return f"data/assets/{obs_type}/{scene}"


def discover_experiments() -> list[tuple[str, str]]:
    """Return (exp_name, obs_path) pairs for all G1CaTra checkpoints found in the log root."""
    experiments = []
    for d in sorted(_LOG_ROOT.glob("*_G1CaTra_*")):
        if not d.is_dir():
            continue
        obs_path = _decode_obs_path(d.name)
        if obs_path is None:
            print(f"  [WARN] Could not decode obs_path from: {d.name} — skipping")
            continue
        if not Path(obs_path).exists():
            print(f"  [WARN] obs_path not found: {obs_path} — skipping {d.name}")
            continue
        experiments.append((d.name, obs_path))
    return experiments


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def build_env(obs_path: str) -> PlayG1CaTraEnv:
    task_cfg = cat_ppo.registry.get("G1CaTra", "config")
    env_cfg = task_cfg.env_config
    env_cfg.pf_config.path = obs_path
    return PlayG1CaTraEnv(task_type="mesh_catra", config=env_cfg, headless=True)


def load_policy(exp_name: str) -> rt.InferenceSession:
    ckpt_path = cat_ppo.get_latest_ckpt(exp_name)
    if ckpt_path is None:
        raise FileNotFoundError(f"No checkpoint found for experiment: {exp_name}")
    onnx_path = ckpt_path / "policy.onnx"
    if not onnx_path.exists():
        raise FileNotFoundError(f"ONNX policy not found: {onnx_path}")
    return rt.InferenceSession(str(onnx_path), providers=["CPUExecutionProvider"])


def _make_camera(distance: float = 5.0, azimuth: float = 135.0, elevation: float = -20.0) -> mujoco.MjvCamera:
    cam = mujoco.MjvCamera()
    cam.type = mujoco.mjtCamera.mjCAMERA_FREE
    cam.distance = distance
    cam.azimuth = azimuth
    cam.elevation = elevation
    cam.lookat[:] = [1.0, 0.0, 0.8]
    return cam


def run_episode(
    env: PlayG1CaTraEnv,
    policy: rt.InferenceSession,
    num_steps: int,
    render_width: int,
    render_height: int,
    cam_distance: float = 5.0,
    cam_azimuth: float = 135.0,
    cam_elevation: float = -20.0,
) -> list[np.ndarray]:
    """Run one episode and return a list of RGB frames."""
    renderer = mujoco.Renderer(env.mj_model, height=render_height, width=render_width)
    cam = _make_camera(cam_distance, cam_azimuth, cam_elevation)
    state = env.reset()
    frames = []

    for _ in range(num_steps):
        obs = state.obs["state"].reshape(1, -1).astype(np.float32)
        action = policy.run(["continuous_actions"], {"obs": obs})[0][0]
        state = env.step(state, action)

        # Track robot horizontally
        pelv_xy = env.mj_data.qpos[:2]
        cam.lookat[0] = pelv_xy[0]
        cam.lookat[1] = pelv_xy[1]

        renderer.update_scene(env.mj_data, camera=cam)
        frames.append(renderer.render())

    renderer.close()
    return frames


def save_video(frames: list[np.ndarray], path: Path, fps: int = 50) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with imageio.get_writer(str(path), fps=fps) as writer:
        for frame in frames:
            writer.append_data(frame)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(description="Run all G1CaTra evaluations and save videos.")
    parser.add_argument("--output_dir", default="eval_videos_catra", help="Output folder for videos")
    parser.add_argument("--num_steps", type=int, default=500, help="Steps per episode (50 Hz → 500 = 10 s)")
    parser.add_argument("--width",  type=int, default=640, help="Render width in pixels")
    parser.add_argument("--height", type=int, default=480, help="Render height in pixels")
    parser.add_argument("--fps",    type=int, default=50,  help="Video frame rate")
    parser.add_argument("--cam_distance",  type=float, default=5.0,   help="Camera distance from lookat point")
    parser.add_argument("--cam_azimuth",   type=float, default=135.0, help="Camera azimuth angle in degrees")
    parser.add_argument("--cam_elevation", type=float, default=-20.0, help="Camera elevation angle (negative = looking down)")
    parser.add_argument("--filter", default="", help="Only run experiments whose name contains this string")
    args = parser.parse_args()

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    experiments = discover_experiments()
    if not experiments:
        print(f"No G1CaTra experiments found in {_LOG_ROOT}/")
        return

    if args.filter:
        experiments = [(n, p) for n, p in experiments if args.filter in n]
        if not experiments:
            print(f"No experiments match filter '{args.filter}'. Available:")
            for name, _ in discover_experiments():
                print(f"  {name}")
            return

    print(f"Running {len(experiments)} G1CaTra evaluations → {output_dir}/\n")

    for i, (exp_name, obs_path) in enumerate(experiments):
        video_path = output_dir / f"{exp_name}.mp4"
        if video_path.exists():
            print(f"[{i+1}/{len(experiments)}] SKIP (exists): {exp_name}")
            continue

        print(f"[{i+1}/{len(experiments)}] {exp_name}")
        print(f"  obs_path: {obs_path}")
        try:
            env = build_env(obs_path)
            policy = load_policy(exp_name)
            frames = run_episode(
                env, policy, args.num_steps, args.width, args.height,
                cam_distance=args.cam_distance,
                cam_azimuth=args.cam_azimuth,
                cam_elevation=args.cam_elevation,
            )
            save_video(frames, video_path, fps=args.fps)
            print(f"  Saved {len(frames)} frames → {video_path}")
        except Exception as exc:
            print(f"  ERROR: {exc}")


if __name__ == "__main__":
    main()
